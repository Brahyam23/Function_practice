# Realiza una función llamada intermedio() 
# que a partir de dos números, devuelva su 
# punto intermedio: Comprueba el punto intermedio entre -12 y 24

def intermedio(num_1:int, num_2:int): #Definimos función

    return (num_1 + num_2) / 2 #Hacemos operación y retornamos resultado

print(intermedio(-12, 24)) #Mostramos en pantalla intermedio de los números dados