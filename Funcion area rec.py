#Realiza una función llamada area_rectangulo() que devuelva el área 
# del rectángulo a partir de una base y una altura. Calcula el área 
# de un rectángulo de 15 de base y 10 de altura

def area_rectangulo(base:int, altura:int): #Definimos función

    return base * altura #Hacemos calculo y retornamos área

print(area_rectangulo(15, 10)) #Mostramos en pantalla rectángulo indicado